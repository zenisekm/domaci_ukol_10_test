package eu.engeto.calculation;

public class Calc {
    public static int scitani(int a, int b) {
        return a + b;
    }

    public static int odcitani(int a, int b) {
        return a - b;
    }

    public static int nasobeni(int a, int b) {
        return a * b;
    }

    public static int deleni(int a, int b) {
        return a / b;
    }

}